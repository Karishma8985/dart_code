/*Relational Operators
This class of operators contain those operators which are used to perform relational operation on the operands. */
void main()
{
   int a = 4;
   int b = 5;

   bool c = a==b;
   print(" a == b: $c");

   c= a!=b;
   print(" a != b: $c");

   c= a<=b;
   print(" a <= b: $c");

   c= a>=b;
   print(" a >= b: $c");

   c= a<b;
   print(" a < b: $c");

   c= a>b;
   print(" a > b: $c");
}